// Send tokens
const sendTokens = async () => {
  const gasPrice = await provider.getGasPrice();
  const gasLimit = 100000;
  const nonce = await wallet.getTransactionCount();
  const tx = await contract.transfer(recipientAddress, amount, { gasPrice, gasLimit, nonce });
  console.log(`Transaction hash: ${tx.hash}`);
  const gasEstimate = await contract.estimateGas.transfer(recipientAddress, amount);
	console.log(`Gas estimate: ${gasEstimate}`);

  contract.on(eventName, (from, to, value) => {
    console.log(`${from} sent ${value} tokens to ${to}`);

});
}


